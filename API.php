		{
			if (file_exists("destaques/".$entrada_array_Filme[$numero_aleatorio].".jpg")) //Checka se existe o filme
			{ 
				$linkFinal = "destaques/".$entrada_array_Filme[$numero_aleatorio].".jpg"; 
